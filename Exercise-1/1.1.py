# one mile is equal to approximately 1.61 kilometers
# Given : distance = 10 km , Time = 43 min 30 sec

distance = 10*1.61 #in miles
time = 43.5/60     #in hours

average_time = distance/43.5
speed = distance/time #average speed in miles per hour 

print(f"Average Time = {average_time}")
print(f"Speed = {speed} mph")