# methods of list
#append -- uses to add an element at the last of the list

a = [1,2,3,4,5,6,"x","y","z"]
a.append("a")
print(a)

#extend -- it is used to add another string at thelast of the list

b = ["b","c","d","e"]
a.extend(b)
print(a)

# sort -- arranges the element in ascending order

c = [3,1,89,7456,65,4,6,891]
c.sort()
print(c)